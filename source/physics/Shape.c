//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// class's constructor
void Shape_constructor(Shape this, InGameEntity owner, int deep){

	// this is an abstract class so must initialize the vtable here
	// since this class does not have an allocator
	__SET_CLASS(Shape);	

	// construct base object
	__CONSTRUCT_BASE(Object);

	// set the owner	
	this->owner = owner;
	
	// set the deep
	this->deep = deep;
	
	// it is active
	this->active = true;
	
	// do I move?
	this->moves = __VIRTUAL_CALL(int, InGameEntity, moves, owner);
	
	// not checked yet
	this->checked = false;
	
	// not setup yet
	this->ready = false;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// class's destructor
void Shape_destructor(Shape this){

	// destroy the super object
	__DESTROY_BASE(Object);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// retrieve owner
InGameEntity Shape_getOwner(Shape this){
	
	return this->owner;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// set active
void Shape_setActive(Shape this, int active){
	
	// it is active
	this->active = active;

}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// is active?
int Shape_isActive(Shape this){
	
	return this->active;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// do I move?
int Shape_moves(Shape this){
	
	return this->moves;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// has been checked
int Shape_isChecked(Shape this){
	
	return this->checked;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// set check status
void Shape_checked(Shape this, int checked){
	
	this->checked = checked;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// has been configured?
int Shape_isReady(Shape this){
	
	return this->ready;
}